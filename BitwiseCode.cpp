
#include<iostream>
using namespace std;
int main()
{
	int a = 7,b = 5;
	/*
		8 4 2 1
		0 1 0 1 => 5
		0 1 1 1 => 7
	*/

	// bitwise AND '&'

// 	   8 4 2 1
// 7 : 0 1 1 1
// 5 : 0 1 0 1
// ------------
// 	0 1 0 1 => 5	
	cout<<a<<" & "<<b<<" = "<<(a&b)<<endl;


// 	   8 4 2 1
// 7 : 0 1 1 1
// 5 : 0 1 0 1
// ------------
// 	   0 1 1 1 => 7
	cout<<a<<" | "<<b<<" = "<<(a|b)<<endl;


// 	   8 4 2 1
// 7 : 0 1 1 1
// 5 : 0 1 0 1
// ------------
//     0 0 1 0 => 2
	cout<<a<<" ^ "<<b<<" = "<<(a^b)<<endl;

     //  x = -(a+1)
		// = -(7+1)
		// = -8 
	cout<<"  ~ "<<a<<" = "<<(~a)<<endl;

	a = 7,b = 3;
	// 32 + 16 + 8 = 56
	/*
		0000 0111  , 7<<3
		0011 1000	
	*/
	// x = a*2^b => 7*2^3 = 7*8 = 56
	cout<<a<<" << "<<b<<" = "<<(a<<b)<<endl;

	a = 7, b = 2;

	// 0000 0111

	// 0000 0001 => 1 
	// x = a/2^b => 7/2^2= 7/4 ~=  1
	cout<<a<<" >> "<<b<<" = "<<(a>>b)<<endl;

}